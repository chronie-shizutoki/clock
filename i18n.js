    currentLocale: navigator.language || 'en-US',
    
    setLocale(locale) {
        this.currentLocale = locale;
        document.dispatchEvent(new CustomEvent('i18nLanguageChanged', { detail: { locale: locale } }));
        this.translatePage();
    },
    
    getMessage(key) {
        return this.messages[this.currentLocale]?.[key] || this.messages['en-US'][key] || key;
    },
    
    translatePage() {
        document.querySelectorAll('[data-i18n]').forEach(element => {
            const key = element.getAttribute('data-i18n');
            element.textContent = this.getMessage(key);
        });
        document.querySelectorAll('[data-i18n-placeholder]').forEach(element => {
            const key = element.getAttribute('data-i18n-placeholder');
            element.placeholder = this.getMessage(key);
        });
    }
